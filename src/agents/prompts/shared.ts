export const writingRules = `
正文生成规则：
1. 不要慢热，不要长篇背景介绍。
2. 300 字内必须出现冲突、悬念、危机或反常信息。
3. 每 1000 到 1500 字必须有信息增量。
4. 每个场景必须推动剧情，不要为了文采牺牲节奏。
5. 第一人称故事要有强代入感。
6. 爽文要有压迫、反击、清算、情绪释放。
7. 悬疑故事要有问题、线索、误导、反转、真相。
8. 言情故事要有关系张力、误会、选择、伤害、补偿或放手。
9. 反派不能只是坏，要有欲望和手段。
10. 主角不能只被动承受，必须逐渐主动行动。
11. 结尾必须兑现开头承诺。
12. 伏笔必须回收，反转必须有铺垫。
13. 不要生成套话、空泛感慨和流水账。
14. 场景之间必须连续：承接上一场最后的事件、情绪和人物状态，并为下一场留下接力点。
15. 少解释，多呈现：优先写动作、对白、表情、停顿、环境反应，不要替角色总结情绪和道理。
16. 不要把角色的每个念头都说透，不要替读者做阅读理解，不要在段尾补一句概括性总结。
17. 避免工整排比、鸡汤式感慨、像文案一样的金句、过度整洁的起承转合。
18. 对白要带目的和火气，人物说话不能都像一个人，能用人话解决就不要用书面总结。
19. 细节必须服务当前冲突，优先写能暴露关系、权力和欲望的细节，不堆砌空镜和形容词。
20. 允许句子有长有短、有停顿、有不顺气的地方，整体目标是像人在讲眼前发生的事，不像模型在完成作文。
`;

